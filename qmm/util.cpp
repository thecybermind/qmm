//Format a string
char* vaf(char* format, ...) {
	va_list	argptr;
	static char str[1024];

	va_start(argptr, format);
	vsnprintf(str, 1024, format, argptr);
	va_end(argptr);

	return str;
}

#ifdef WIN32
 #undef vsnprintf
#endif

//find CPlugin with given id, return ptr
CPlugin* findplugin(int id) {
	int i = 0;
	CLinkNode<CPlugin>* p = Plugins.get_first();

	ENGFUNC(G_PRINT, vaf("[QMM] Searching for plugin with id \"%i\"\n", id));

	while (p) {
		if (i == id) {
			ENGFUNC(G_PRINT, vaf("[QMM] Found plugin \"%s\" with id \"%i\"\n", p->data->get_plugininfo()->name, id));
			return p->data;
		}
		++i;
		p = p->next;
	}

	ENGFUNC(G_PRINT, vaf("[QMM] Could not find plugin with id \"%i\"\n", id));

	return NULL;
}

